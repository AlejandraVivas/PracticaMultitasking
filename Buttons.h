#ifndef SOURCE_BUTTONS_H_
#define SOURCE_BUTTONS_H_

/*Buttons value in port c*/
#define BUTTON_ONE 0x20 /*Bit 5, port C 0010_0000*/
#define BUTTON_TWO 0x80 /*Bit 7, port C, 1000_0000*/
#define BUTTON_THREE 0x0 /*Bit 0, port C, 0000_0000*/
#define BUTTON_FOUR 0x200 /*Bit 9, port C, 0010_0000_0000*/
#define BUTTON_FIVE 0x100 /*Bit 8, port C, 0001_0000_0000*/
#define BUTTON_SIX 0x8 /*Bit 3, port C, 0000_1000*/



#endif /* SOURCE_BUTTONS_H_ */
